    private void txtTenKMKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTenKMKeyReleased
        // TODO add your handling code here:
        if (!txtTenKM.getText().matches("^[0-9a-zA-Z]*$")) {
            JOptionPane.showMessageDialog(this, "Không được nhập kí tự đặc biệt", "Sai kiểu dữ liệu", JOptionPane.INFORMATION_MESSAGE);
            txtTenKM.setText("");
        }
    }//GEN-LAST:event_txtTenKMKeyReleased

