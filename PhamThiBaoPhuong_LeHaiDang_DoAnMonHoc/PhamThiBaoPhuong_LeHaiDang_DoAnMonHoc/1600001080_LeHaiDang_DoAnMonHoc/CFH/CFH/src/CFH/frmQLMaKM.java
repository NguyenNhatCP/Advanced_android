    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        btnCancel.setEnabled(true);
        DefaultTableModel model = (DefaultTableModel) tbListMaKM.getModel();
        if (tbListMaKM.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn khuyến mãi muốn sửa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            setButtonKM(false);
            setEnableKM(true);
            add = false;
            txtTenKM.requestFocus();
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbListMaKM.getModel();
        if (tbListMaKM.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn khuyến mãi muốn xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                int ma = Integer.parseInt(txtMaKM.getText());
                c.DeleteKM(ma);
                JOptionPane.showMessageDialog(this, "Bạn đã xóa khuyến mãi thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                model.setRowCount(0);
                showListMaKM();
            } catch (SQLException sql) {

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int pt = 0;
        DefaultTableModel model = (DefaultTableModel) tbListMaKM.getModel();
        if (add) {
            try {
                if (txtNoiDung.getText().equals("") || txtTenKM.getText().equals("") || txtPhanTram.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Hãy nhập đầy đủ các ô", "Thông báo", JOptionPane.INFORMATION_MESSAGE);  
                } else {
                    pt = Integer.parseInt(txtPhanTram.getText());
                    c.InsertKM(txtTenKM.getText(), pt, txtNoiDung.getText());
                    JOptionPane.showMessageDialog(this, "Bạn đã thêm sản phẩm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    model.setRowCount(0);
                    setEnableKM(false);
                    setButtonKM(true);
                    setNullKM();
                    showListMaKM();
                }

            } catch (SQLException sql) {

            }
        } else {
            int ma = 0;
            try {
                if (txtNoiDung.getText().equals("") || txtTenKM.getText().equals("") || txtPhanTram.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Hãy nhập đầy đủ các ô", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    ma = Integer.parseInt(txtMaKM.getText());
                    c.UpdateKM(ma, txtTenKM.getText(), pt, txtNoiDung.getText());
                    JOptionPane.showMessageDialog(this, "Bạn đã sửa sản phẩm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    model.setRowCount(0);
                    setEnableKM(false);
                    setButtonKM(true);
                    setNullKM();
                    showListMaKM();
                }

            } catch (SQLException sql) {

            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

